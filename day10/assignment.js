//대입 연산
let x = 10;

x += 10 ;
console.log(x); // 20
x =+ 10 ; // x = x + 10;
console.log(x); // 10

let y = 10;
y -= 5; // y = y - 5;
console.log(y); // 5

let z = 10;
z *= 5; // z = z * 2;
console.log(z); // 50

let u = 10;
u /= 5; // u = u / 2;
console.log(u); // 2

let v = 10;
v %= 3; // v = v % 2;
console.log(v); // 1

let t = 10;
t **= 2; // t = t ** 2;
console.log(t); // 100
